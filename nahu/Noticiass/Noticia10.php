                En una entrevista con Game Developer (vía GameSpot) durante la Gamescom Asia, Morgan Juinio —quien formó
                parte de Santa Monica Studio durante el desarrollo de God of War (2018)— expresó su preocupación por la
                "toma de decisiones insostenible" que ha caracterizado a la industria tras la pandemia. La creativa
                subraya que, aunque las grandes franquicias como Call of Duty o Battlefield siguen siendo rentables, los
                jugadores también buscan experiencias diferentes: juegos con presupuestos más ajustados, duraciones más
                razonables y desarrollos menos inflados. En su opinión, los gigantes del sector deberían "reflexionar
                sobre qué tipo de producto quieren ofrecer" y planificar de forma consciente hacia una industria más
                equilibrada.
            </p>

            <p>
                Como ejemplo, Meghan Morgan citó proyectos como Astro Bot y Split Fiction, títulos que encajan dentro
                del modelo AA gracias a su menor escala, presupuestos controlados y propuestas interesantes. Para la
                productora, estos juegos han tenido éxito recientemente no solo por ser más accesibles en precio, sino
                porque ofrecen experiencias frescas y creativas que muchos lanzamientos AAA han perdido en su búsqueda
                por el realismo y la grandiosidad.
