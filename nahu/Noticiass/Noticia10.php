                La industria del videojuego atraviesa un momento complicadísimo, con despidos masivos y proyectos cada
                vez más caros y arriesgados. En este contexto, la exproductora de God of War Meghan Morgan Juinio ha
                lanzado un mensaje claro a las grandes compañías como Sony o Activision: es momento de diversificar.
                Según la desarrolladora, las editoras de títulos AAA necesitan abrir espacio a producciones más
                pequeñas, de corte AA o incluso indie, que permitan mantener la innovación y reducir los riesgos que hoy
                amenazan al sector.
