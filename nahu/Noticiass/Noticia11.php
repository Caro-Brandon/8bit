                desagradables. Pero es, en resumidas cuentas, una estética de pesadilla tan característica como
                resultona. El mal sueño que desdibuja la realidad, que se vive bajo la perspectiva de dos niños.
            </p>

            <p>
                Esta nueva entrega focaliza su atención en dos críos, el pequeño Low y la niña Alone. En la línea de la
                saga, el diseño narrativo no se ha apoyado en diálogos, sino en una forma de contar historias que se
                nutre de las imágenes, a veces de forma sutil y siempre de manera muy visual. La estética, que mezcla
                elementos surrealistas y grotescos, presenta también diseños que podríamos calificar como “monos”, que
                se plasman sobre todo en el dúo protagonista y en algunos personajes secundarios. Los enemigos, en
                cambio, siempre lucen monstruosos y terroríficos.
            </p>

            <p>
                Ya nos hemos pasado Little Nightmares 3 y este es nuestro análisis breve con nota. Puede que Little
