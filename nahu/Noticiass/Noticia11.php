                estado implicado en su proceso creativo. La obra de Supermassive Games sigue las directrices de los dos
                anteriores juegos, pero en esta ocasión, los protagonistas son dos niños inmersos en su propia
                pesadilla. Un cuento grotesco, retorcido, con la misma estética característica de la saga, que nos lleva
                a recorrer niveles que se desarrollan principalmente en desplazamiento lateral: plataformas, pequeños
                puzles colaborativos, alguna persecución y unas pocas secuencias de acción. Es un juego entretenido, que
                te mantiene a los mandos, pero que acaba demasiado pronto. Además, hay ciertas imprecisiones de control
                en el salto y a la hora de usar el arco. Lo que celebramos es que por fin se haya añadido un modo
