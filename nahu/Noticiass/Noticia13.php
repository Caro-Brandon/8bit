                imágenes y más, según relata Cyber Daily. Se trata de información sobre Marvel’s Wolverine, así como
                otros proyectos. Al tratarse de una acción delictiva, en MeriStation hemos tomado la decisión de no
                entrar en detalle sobre el contenido en sí. Lo que sí podemos decir es que la información incluye datos
                sensibles de los empleados: informes fiscales, mensajes internos, pasaportes, etc.
            </p>

            <p>
                “No vendido [sic], los datos han sido subidos. Cazadores de datos, disfrutad”, dice el mensaje publicado
                por los hackers. Parece que han llegado a vender algunos datos, ya que han subido el 98% de los archivos
                que están en su mano, aunque la identidad del comprador no ha trascendido. Los archivos se subieron en
                tres paquetes, cada enlace debidamente etiquetado. Por otro lado, las filtraciones no solo incluyen
                documentos, sino también imágenes de diseño y modelados de personajes del juego de Wolverine y de
                Marvel’s Spider-Man 2.
            </p>

            <p>
                Uno de los documentos filtrados es el acuerdo entre Sony Interactive Entertainment y Marvel, firmado
