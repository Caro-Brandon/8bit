                escena de Counter-Strike como ohnePixel, TimTheTatman, Arrow, Nadeshot, Heyzeus y Sparkles
                protagonizaron la maratón de aperturas, que alcanzó un pico de 137.000 espectadores simultáneos en el
                canal de ohnePixel, quien transmitió durante más de 10 horas.
            </p>

            <p>
                Sin embargo, cuando se analizaron los resultados, quedó claro que el fervor generailidad por esta
                apertura masiva no fue rentable en términos monetarios. El usuario RemixFloats, conocido por su
                conocimiento del juego, calculó que el total de ganancias en objetos obtenidos por los streamers fue de
                94.538 dólares, muy por debajo del medio millón invertido. TimTheTatman fue el que más recuperó, con
                30.707 dólares en ítems, mientras que Nadeshot cerró la lista con solo 9.302 dólares a su favor.
